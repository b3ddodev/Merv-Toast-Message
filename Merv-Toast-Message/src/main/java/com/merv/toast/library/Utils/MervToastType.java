package com.merv.toast.library.Utils;

/**
 * Enum class that defines different types of toasts used in the application.
 * These constants represent various toast messages to be displayed based on different situations.
 */
public enum MervToastType {
    /**
     * Success toast type, typically used to indicate a successful operation.
     */
    MERV_TYPE_SUCCESS,

    /**
     * Error toast type, typically used to indicate an error occurred during an operation.
     */
    MERV_TYPE_ERROR,

    /**
     * Warning toast type, typically used to give a warning message about potential issues.
     */
    MERV_TYPE_WARNING,

    /**
     * Info toast type, typically used to display informational messages.
     */
    MERV_TYPE_INFO
}
